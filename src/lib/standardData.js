// 1 sak = 50 kg = 50000 gram
export const SACK_WEIGHT_KG = 50
export const SACK_WEIGHT_GRAM = 50000

/**
 * Calculate daily feed consumption in grams from sack data
 * @param {number} sacksAdded - jumlah sak yang ditambahkan
 * @param {number} sacksRemaining - jumlah sak yang tersisa
 * @param {number} previousRemaining - sisa sak dari hari sebelumnya
 * @returns {number} total feed consumed in grams
 */
export function calcTotalFeedConsumed(sacksAdded, sacksRemaining, previousRemaining = 0) {
    const totalAvailable = (previousRemaining + sacksAdded) * SACK_WEIGHT_GRAM
    const remaining = sacksRemaining * SACK_WEIGHT_GRAM
    return totalAvailable - remaining
}

/**
 * Calculate cumulative feed intake per bird (gram)
 * totalFeedConsumed / currentPopulation
 */
export function calcCumulativeFeedIntake(totalFeedConsumedGram, currentPopulation) {
    if (!currentPopulation || currentPopulation <= 0) return 0
    return totalFeedConsumedGram / currentPopulation
}

/**
 * Calculate FCR
 * cumFeedIntake / bw
 */
export function calcFCR(cumFeedIntake, bw) {
    if (!bw || bw <= 0) return 0
    return cumFeedIntake / bw
}

/**
 * Calculate daily gain (gram)
 * bw today - bw yesterday
 */
export function calcDailyGain(bwToday, bwYesterday) {
    if (bwYesterday === null || bwYesterday === undefined) return 0
    return bwToday - bwYesterday
}

/**
 * Convert gram to kg
 */
export function gramToKg(gram) {
    return gram / 1000
}

/**
 * Convert kg to gram
 */
export function kgToGram(kg) {
    return kg * 1000
}

/**
 * Truncate number to specific decimals without rounding.
 * e.g. 1.456 -> 1.45 (for 2 decimals)
 */
export function truncateNumber(num, decimals = 2) {
    if (num === null || num === undefined || isNaN(num)) return ''
    const factor = Math.pow(10, decimals)
    return (Math.trunc(num * factor) / factor).toFixed(decimals)
}

/**
 * Format number with unit
 */
export function formatValue(value, unit = 'gram', decimals = 2) {
    if (value === null || value === undefined) return '-'
    const num = unit === 'kg' ? gramToKg(value) : value
    return truncateNumber(num, decimals)
}
